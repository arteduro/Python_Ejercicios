#Método reverse():se encarga de invertir los elemntos de una lista.

vocales = ["a", "e", "i", "o", "u",]
print(f"lista: {vocales}")

vocales.reverse()
print(f"\nLista: {vocales}")
print(f"Lista: {vocales}")
print(f"posición 0: {vocales[0]}")

vocales.reverse()
print(f"\nLista: {vocales}")
print(f"posición 0: {vocales[0]}")

